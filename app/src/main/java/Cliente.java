public class Cliente {

    int IdCli;
    String NomeCli;
    String Telefone;

    public int getIdCli() {
        return IdCli;
    }

    public void setIdCli(int idCli) {
        IdCli = idCli;
    }

    public String getNomeCli() {
        return NomeCli;
    }

    public void setNomeCli(String nomeCli) {
        NomeCli = nomeCli;
    }

    public String getTelefone() {
        return Telefone;
    }

    public void setTelefone(String telefone) {
        Telefone = telefone;
    }
}